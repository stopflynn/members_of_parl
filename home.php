				<h1>Members of Parliament: Home</h1>
				<p>Discover the dedicated individuals representing you in the New Zealand Parliament for 2024. Our Members of Parliament (MPs) are committed to serving their constituents and the nation, bringing diverse perspectives and expertise to the legislative process.</p>
				<h2>Who Are the MPs?</h2>
				<p>The New Zealand Parliament consists of 120 MPs elected through a mixed-member proportional (MMP) electoral system. This system ensures that the composition of Parliament closely reflects the votes of the electorate, combining both electorate MPs and list MPs.</p>
				<h2>Roles and Responsibilities</h2>
				<p>MPs play a vital role in shaping New Zealand's future. Their key responsibilities include:</p>
				<ul>
					<li><strong>Legislation:</strong> Debating and voting on proposed laws to ensure they serve the best interests of all New Zealanders.</li>
					<li><strong>Representation:</strong> Acting as the voice of their constituents, bringing local issues to national attention and ensuring that community concerns are addressed.</li>
					<li><strong>Oversight:</strong> Holding the government accountable through questions, debates, and participation in select committees that review policies, legislation, and public expenditure.</li>
					<li><strong>Advocacy:</strong> Championing the rights and interests of New Zealanders on various issues, from healthcare and education to the environment and economy.</li>
				</ul>

