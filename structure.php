    <h1>Structure of the New Zealand Parliament</h1>
    <p>The New Zealand Parliament is a unicameral system, meaning it has only one legislative chamber, the House of Representatives. It also includes the Sovereign, represented by the Governor-General. The key components of this structure are as follows:</p>
    
    <h2>1. The Sovereign</h2>
    <ul>
        <li><strong>Role:</strong> The Sovereign is the formal head of state, and their role in Parliament is mostly ceremonial. The current monarch of New Zealand is also the monarch of the United Kingdom.</li>
        <li><strong>Governor-General:</strong> The Sovereign is represented in New Zealand by the Governor-General, who performs various ceremonial duties, such as giving royal assent to bills, opening and dissolving Parliament, and appointing the Prime Minister.</li>
    </ul>
    
    <h2>2. The House of Representatives</h2>
    <ul>
        <li><strong>Members:</strong> The House of Representatives is composed of 120 Members of Parliament (MPs). These MPs are elected every three years.</li>
        <li><strong>Elections:</strong> MPs are elected through a mixed-member proportional (MMP) electoral system, where voters have two votes: one for a political party and one for their local electorate MP.
         <li><strong>Party Vote:</strong> Determines the overall number of seats each political party receives in the House.</li>
        <li><strong>Electorate Vote:</strong> Determines the local representatives for geographical electorates.</li>
        </li>
        <li><strong>Majority:</strong> The party or coalition with the majority of seats in the House forms the government. The leader of this party or coalition becomes the Prime Minister.</li>
    </ul>
    
    <h2>Functions of the House of Representatives</h2>
    <h3>1. Legislation</h3>
    <ul>
        <li><strong>Bills:</strong> Proposed laws (bills) are introduced, debated, and voted on in the House. They must pass several readings and stages, including committee scrutiny, before becoming law.</li>
        <li><strong>Royal Assent:</strong> Once a bill passes all stages, it is sent to the Governor-General for royal assent and becomes an Act of Parliament.</li>
    </ul>
    
    <h3>2. Government Formation</h3>
    <ul>
        <li><strong>Prime Minister:</strong> The Governor-General appoints the leader of the majority party or coalition as Prime Minister.</li>
        <li><strong>Cabinet:</strong> The Prime Minister selects members of the Cabinet, who are responsible for different government departments and portfolios.</li>
    </ul>
    
    <h3>3. Oversight and Accountability</h3>
    <ul>
        <li><strong>Question Time:</strong> MPs question the Prime Minister and other ministers about their policies and decisions.</li>
        <li><strong>Debates:</strong> MPs debate bills, motions, and other issues of national importance.</li>
        <li><strong>Select Committees:</strong> Specialized committees review legislation, investigate issues, and oversee government activities.</li>
    </ul>
    
    <h3>4. Representation</h3>
    <ul>
        <li><strong>Constituents:</strong> MPs represent the interests and concerns of their constituents, bringing local issues to national attention.</li>
        <li><strong>Public Participation:</strong> The public can participate in the legislative process by submitting petitions, making submissions to select committees, and engaging with their MPs.</li>
    </ul>
    
    <h2>Select Committees</h2>
    <ul>
        <li><strong>Composition:</strong> Select committees are made up of MPs from different parties and reflect the composition of the House.</li>
        <li><strong>Functions:</strong> They examine bills in detail, review government policy and administration, consider petitions, and investigate specific issues.</li>
        <li><strong>Public Engagement:</strong> Select committees often invite public submissions and hold hearings, allowing citizens to contribute to the legislative process.</li>
    </ul>
    
    <p>This structure ensures that New Zealand's parliamentary system is democratic, transparent, and representative of its citizens' diverse views.</p>

				
